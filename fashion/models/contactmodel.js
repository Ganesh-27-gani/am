import mongoose from "mongoose";

const contactSchema = new mongoose.Schema({

    fullname: {
        type: String,
        required: true,
        trim: true,
    },
    email: {
        type: String,
        required: true,
        trim: true,
    },
    phone: {
        type: String,
        required: true,
        trim: true,
    },
    subject: {
        type: String,
        required: true,
        trim: true,
    },
    message: {
        type: String,
        required: true,
        trim: true,
    },
},
    {
        timestamps: true,

    }
)

export default mongoose.model("contact", contactSchema)